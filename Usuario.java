public class Usuario {
    private String nome;
    private String sobrenome;
    private int idade;
    private String email;
    private String cpf;

    public Usuario (String nome, String sobrenome, int idade, String email, String cpf){
        this.nome = nome;
        this.sobrenome = sobrenome;
        this.idade = idade;
        this.email = email;
        this.cpf = cpf;
    }
}
